#define AGL_COUNT (sizeof(agl_entries) / sizeof(agl_entries[0]))

/* Binary search the Adobe Glyph List */
static uint16_t agl_lookup(const char *name) {
    int lo = 0, hi = (int)AGL_COUNT - 1;
    while (lo <= hi) {
        int mid = (lo + hi) / 2;
        int cmp = strcmp(name, agl_entries[mid].name);
        if (cmp == 0) return agl_entries[mid].unicode;
        if (cmp < 0) hi = mid - 1;
        else lo = mid + 1;
    }
    return 0;
}

